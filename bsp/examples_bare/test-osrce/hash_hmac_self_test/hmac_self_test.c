u32 hmac_test_internal(HASH_ALG hash_alg, u8 *std_mac1, u8 *std_mac2)
{
    uint16_t key_id = 0;
    u8 key[144];
    u8 message[100];
    u8 mac[32];

    u32 __attribute__((unused)) i, j, total, block_byte_len, digest_byte_len;
    u32 ret;

    block_byte_len = hash_get_block_word_len(hash_alg)<<2;
    digest_byte_len = hash_get_digest_word_len(hash_alg)<<2;;

    memcpy_(message, (u8 *)"abc", 3);   //to avoid some bad compiler's error(memset not found)

    for(i=0;i<block_byte_len;i++)
    {
        key[i] = i;
    }

    /*************************** test1 ***************************/
    ret = hmac(hash_alg, key, key_id, 3, message, 3, mac);
    if(HASH_SUCCESS != ret || memcmp_(mac, std_mac1, digest_byte_len))
    {
        //printf("\r\n %s test 1 failure, ret =%02x \r\n", digest_byte_len, ret);
        //print_buf_U8(key, 3, "key");
        //print_buf_U8(message, 3, "message");
        //print_buf_U8(mac, digest_byte_len, "mac");
        return 1;
    }
    else
    {
        //printf("\r\n test 1 success ");
    }

    /*************************** test2 ***************************/
    for(i=0;i<100;i++)
    {
        message[i] = i;
    }

    ret = hmac(hash_alg, key, key_id, block_byte_len, message, 100, mac);
    if(HASH_SUCCESS != ret || memcmp_(mac, std_mac2, digest_byte_len))
    {
        //printf("\r\n %s test 2 failure, ret =%02x \r\n", digest_byte_len, ret);
        //print_buf_U8(key, block_byte_len, "key");
        //print_buf_U8(message, 100, "message");
        //print_buf_U8(mac, digest_byte_len, "mac");
        return 2;
    }
    else
    {
        //printf("\r\n test 2 success ");
    }

    return 0;
}

