typedef struct
{
    u8 alg;
    u8 test_mode;
    u8 *std_in;
    u32 std_bytes;
    u8 *std_key;
    uint16_t key_id;
    u8 *std_iv;
    u8 *std_out;
}SKE_BASIC_MODE_SELF_TEST_T;

u32 ske_basic_mode_test_internal(SKE_ALG ske_alg, SKE_MODE mode, u8 *std_plain, u32 byteLen,
        u8 *key, uint16_t key_id, u8 *iv, u8 *std_cipher)
{
    u8 cipher[32];
    u8 replain[32];
    u32 out_len;
    int8_t ret;

    //u32 key_byteLen   = ske_sec_get_key_byte_len(ske_alg);
    //u32 block_byteLen = ske_sec_get_block_byte_len(ske_alg);

    //ENCRYPT
    ret = ske_sec_crypto(ske_alg, mode, SKE_CRYPTO_ENCRYPT, key, key_id, iv, SKE_NO_PADDING, (u8 *)std_plain, cipher, byteLen, &out_len);
    if(SKE_SUCCESS != ret)
    {
        return 1;
    }

    if(memcmp_(cipher, std_cipher, byteLen))
    {
        //printf("\r\n ske enc test failure!!");
        //print_buf_U8(std_plain, byteLen, "std_plain");
        //print_buf_U8(std_cipher, byteLen, "std_cipher");
        //print_buf_U8(cipher, byteLen, "cipher");
        return 2;
    }

    //DECRYPT
    ret = ske_sec_crypto(ske_alg, mode, SKE_CRYPTO_DECRYPT, key, key_id, iv, SKE_NO_PADDING, cipher, replain, byteLen, &out_len);
    if(SKE_SUCCESS != ret)
    {
        return 3;
    }

    if(memcmp_(replain, std_plain, byteLen))
    {
        //printf("\r\n ske test failure!!");
        //print_buf_U8(std_plain, byteLen, "std_plain");
        //print_buf_U8(std_cipher, byteLen, "std_cipher");
        //print_buf_U8(cipher, byteLen, "cipher");
        //print_buf_U8(replain, byteLen, "replain");
        return 4;
    }
    else
    {
        //printf("\r\n ske test success!!");
    }

    return 0;
}

u32 ske_basic_mode_self_test_vector(SKE_BASIC_MODE_SELF_TEST_T *ske_t, u32 test_num)
{
    u32 ret = 0;

    for(u32 i = 0; i <test_num; i++)
    {
        ret = ske_basic_mode_test_internal((SKE_ALG)ske_t[i].alg, (SKE_MODE)ske_t[i].test_mode, (u8 *)ske_t[i].std_in, ske_t[i].std_bytes,
                (u8 *)ske_t[i].std_key, ske_t[i].key_id, (u8 *)ske_t[i].std_iv, (u8 *)ske_t[i].std_out);
        if(0 != ret)
        {
            return ret;
        }
        else
        {;}
    }

    return 0;
}

