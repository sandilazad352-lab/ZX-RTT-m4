u32 hash_test_internal(HASH_ALG hash_alg, u8 *std_digest1,u8 *std_digest2)
{
    u8 message[100];
    u8 digest[64];
    u32 digest_byte_len;

    u32 i, ret;

    digest_byte_len = hash_get_digest_word_len(hash_alg)<<2;

    memcpy_(message, (u8 *)"abc", 3);   //to avoid some bad compiler's error(memset not found)


    /*************************** test1 ***************************/
    ret = hash(hash_alg, message, 3, digest);
    if(HASH_SUCCESS != ret || memcmp_(digest, std_digest1, digest_byte_len))
    {
        //printf("\r\n test 1 failure, ret=%d\r\n", ret);
        //print_buf_U8(message, 3, "message");
        //print_buf_U8(digest, digest_byte_len, "digest");
        return 1;
    }
    else
    {
        //printf("\r\n test 1 success ");
    }

    /*************************** test2 ***************************/
    for(i=0;i<100;i++)
    {
        message[i] = i;
    }

    //test2-1
    ret = hash(hash_alg, message, 100, digest);
    if(HASH_SUCCESS != ret || memcmp_(digest, std_digest2, digest_byte_len))
    {
        //printf("\r\n test 2 failure, ret=%d \r\n", ret);
        //print_buf_U8(message, 100, "message");
        //print_buf_U8(digest, digest_byte_len, "digest");
        return 2;
    }
    else
    {
        //printf("\r\n test 2 success ");
    }

    return 0;
}

