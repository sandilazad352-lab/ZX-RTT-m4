#define PANEL_DEV_NAME      "dsi_panel_fl7705n"

static int panel_enable(struct aic_panel *panel)
{
    int ret;

    panel_di_enable(panel, 0);
    panel_dsi_send_perpare(panel);

    panel_dsi_dcs_send_seq(panel, 0xB9,0xF1,0x12,0x87);
    panel_dsi_dcs_send_seq(panel, 0xB2,0x40,0x15,0x78);
    panel_dsi_dcs_send_seq(panel, 0xB3,0x10,0x10,0x28,0x28,0x03,0xFF,0x00,0x00,0x00,0x00);
    panel_dsi_dcs_send_seq(panel, 0xB4,0x80);
    panel_dsi_dcs_send_seq(panel, 0xB5,0x0A,0x0A);
    panel_dsi_dcs_send_seq(panel, 0xB6,0x9D,0x9D);
    panel_dsi_dcs_send_seq(panel, 0xBA,0x33,0x81,0x05,0xF9,0x0E,0x0E,0x20,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x44,0x25,0x00,0x91,0x0A,0x00,0x00,0x01,0x4F,0x01,0x00,0x00,0x37);
    panel_dsi_dcs_send_seq(panel, 0xBC,0x47);
    panel_dsi_dcs_send_seq(panel, 0xBF,0x02,0x10,0x00,0x80,0x04);
    panel_dsi_dcs_send_seq(panel, 0xC0,0x73,0x73,0x50,0x50,0x00,0x00,0x12,0x73,0x00);
    panel_dsi_dcs_send_seq(panel, 0xC1,0x57,0x00,0x32,0x32,0x77,0xE4,0x77,0x77,0xCC,0xCC,0xFF,0xFF,0x11,0x11,0x00,0x00,0x32);
    panel_dsi_dcs_send_seq(panel, 0xC7,0x10,0x00,0x0A,0x00,0x00,0x00,0x00,0x00,0xED,0xC5,0x00,0xA5);
    panel_dsi_dcs_send_seq(panel, 0xC8,0x10,0x40,0x1E,0x03);
    panel_dsi_dcs_send_seq(panel, 0xCC,0x0B);
    panel_dsi_dcs_send_seq(panel, 0xE0,0x00,0x00,0x00,0x1B,0x25,0x3F,0x24,0x1C,0x05,0x0A,0x0C,0x0E,0x10,0x0E,0x11,0x11,0x17,0x00,0x00,0x00,0x1B,0x25,0x3F,0x24,0x1C,0x05,0x0A,0x0C,0x0E,0x10,0x0E,0x11,0x11,0x17);
    panel_dsi_dcs_send_seq(panel, 0xE1,0x11,0x11,0x91,0x00,0x00,0x00,0x00);
    panel_dsi_dcs_send_seq(panel, 0xE3,0x07,0x07,0x0B,0x0B,0x0B,0x0B,0x00,0x00,0x00,0x00,0xFF,0x84,0xC0,0x10);
    panel_dsi_dcs_send_seq(panel, 0xE9,0xC8,0x10,0x07,0x05,0x02,0x80,0x81,0x12,0x31,0x23,0x4F,0x86,0x80,0x81,0x47,0x16,0x00,0x00,0x05,0x00,0x00,0x00,0x00,0x00,0x05,0x00,0x00,0x00,0x48,0x18,0xFA,0xB3,0x17,0x58,0x88,0x88,0x88,0x88,0x88,0x48,0x08,0xFA,0xB2,0x06,0x48,0x88,0x88,0x88,0x88,0x88,0x00,0x00,0x00,0x01,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00);
    panel_dsi_dcs_send_seq(panel, 0xEA,0x96,0x12,0x01,0x01,0x02,0x96,0x00,0x00,0x00,0x00,0x00,0x00,0x4F,0x08,0x8A,0xB4,0x60,0x28,0x88,0x88,0x88,0x88,0x88,0x4F,0x18,0x8A,0xB5,0x71,0x38,0x88,0x88,0x88,0x88,0x88,0x23,0x00,0x00,0x00,0xB2,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x40,0x80,0x81,0x00,0x00,0x00,0x00);
    panel_dsi_dcs_send_seq(panel, 0xEF,0xFF,0xFF,0x01);

    ret = panel_dsi_dcs_exit_sleep_mode(panel);
    if (ret < 0) {
        pr_err("Failed to exit sleep mode: %d\n", ret);
        return ret;
    }

    aic_delay_ms(200);

    ret = panel_dsi_dcs_set_display_on(panel);
    if (ret < 0) {
        pr_err("Failed to set display on: %d\n", ret);
        return ret;
    }

    aic_delay_ms(120);

    panel_dsi_setup_realmode(panel);

    panel_de_timing_enable(panel, 0);
    panel_backlight_enable(panel, 0);
    return 0;
}

static struct aic_panel_funcs panel_funcs = {
    .disable = panel_default_disable,
    .unprepare = panel_default_unprepare,
    .prepare = panel_default_prepare,
    .enable = panel_enable,
    .register_callback = panel_register_callback,
};

