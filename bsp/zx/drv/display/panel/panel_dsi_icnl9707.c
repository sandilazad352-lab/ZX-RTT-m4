#include "panel_com.h"
#include "panel_dsi.h"

#define PANEL_DEV_NAME      "dsi_panel_icnl9707"

static int panel_enable(struct aic_panel *panel)
{
    int ret;

    panel_di_enable(panel, 0);
    panel_dsi_send_perpare(panel);

    panel_dsi_dcs_send_seq(panel,0xF0,0x5A,0x59);
    panel_dsi_dcs_send_seq(panel,0xF1,0xA5,0xA6);
    //FORWARD SCAN
    panel_dsi_dcs_send_seq(panel,0xB4,0x00,0x01,0x08,0x10,0x11,0x12,0x13,0x0C,0x0D,0x0E,0x0F,0x00,0x1E,0x1F,0x04,0x02,0x02,0x02,0x02,0x02,0x02,0x02);
    //BACKWARD SCAN
    //panel_dsi_dcs_send_seq(panel,0x01,0xB4,0x00,0x01,0x08,0x0F,0x0E,0x0D,0x0C,0x13,0x12,0x11,0x10,0x00,0x1E,0x1F,0x04,0x02,0x02,0x02,0x02,0x02,0x02,0x02);
    panel_dsi_dcs_send_seq(panel,0xB0,0x60,0x00,0x80,0x00,0x66,0x66,0x33,0x33,0x0A,0x00,0x00,0x56,0x00,0x00,0x0F);
    panel_dsi_dcs_send_seq(panel,0xB1,0x53,0xA0,0x00,0x85,0x0A,0x00,0x00,0x56,0x00,0x00);
    panel_dsi_dcs_send_seq(panel,0xB2,0x37,0x09,0x08,0x8B,0x08,0x11,0x11,0x11,0x11,0x80);
    panel_dsi_dcs_send_seq(panel,0xB6,0x50,0x50);
    panel_dsi_dcs_send_seq(panel,0xB7,0x01,0x01,0x09,0x0D,0x11,0x19,0x1D,0x15,0x00,0x25,0x21,0x00,0x00,0x00,0x00,0x02,0xF7,0x38);
    //panel_dsi_dcs_send_seq(panel,0x01,0xB8,0x34,0x53,0x02,0xCC,0x//V0.5);
    panel_dsi_dcs_send_seq(panel,0xB8,0x34,0x53,0x02,0xCC);
    panel_dsi_dcs_send_seq(panel,0xBA,0x12,0xD3);// 第一个参数12：POWER IC：7723/1002 ,27:YL5019X/7721
    panel_dsi_dcs_send_seq(panel,0xBD,0x43,0x0E,0x0E,0x4B,0x4B,0x14,0x14);
    //panel_dsi_dcs_send_seq(panel,0xC0,0x11); //bist mode
    panel_dsi_dcs_send_seq(panel,0xC1,0x00,0x0C,0x20,0x04,0x00,0x32,0x32,0x04);
    panel_dsi_dcs_send_seq(panel,0xC2,0x31,0xC0);
    panel_dsi_dcs_send_seq(panel,0xC3,0x22,0x31);
    panel_dsi_dcs_send_seq(panel,0xC6,0x00,0x00,0xFF,0x00,0x00,0xFF,0x00,0x00);
    //GAMMA2.2
    panel_dsi_dcs_send_seq(panel,0xC8,0x7C,0x6B,0x5E,0x53,0x51,0x44,0x48,0x32,0x4B,0x4A,0x49,0x67,0x54,0x5B,0x4C,0x46,0x38,0x23,0x06,0x7C,0x6B,0x5E,0x53,0x51,0x44,0x48,0x32,0x4B,0x4A,0x49,0x67,0x54,0x5B,0x4C,0x46,0x38,0x23,0x06);
    panel_dsi_dcs_send_seq(panel,0xD0,0x07,0xFF,0xFF);
    panel_dsi_dcs_send_seq(panel,0xD2,0x63,0x0B,0x08,0x88);
    panel_dsi_dcs_send_seq(panel,0xD4,0x00,0x00,0x00,0x32,0x04,0x51);
    panel_dsi_dcs_send_seq(panel,0xF1,0x5A,0x59);
    panel_dsi_dcs_send_seq(panel,0xF0,0xA5,0xA6);
    panel_dsi_dcs_send_seq(panel,0x35,0x00);

    ret = panel_dsi_dcs_exit_sleep_mode(panel);
    if (ret < 0) {
        pr_err("Failed to exit sleep mode: %d\n", ret);
        return ret;
    }

    aic_delay_ms(200);

    ret = panel_dsi_dcs_set_display_on(panel);
    if (ret < 0) {
        pr_err("Failed to set display on: %d\n", ret);
        return ret;
    }

    aic_delay_ms(120);

    panel_dsi_setup_realmode(panel);

    panel_de_timing_enable(panel, 0);
    panel_backlight_enable(panel, 0);
    return 0;
}

static struct aic_panel_funcs panel_funcs = {
    .disable = panel_default_disable,
    .unprepare = panel_default_unprepare,
    .prepare = panel_default_prepare,
    .enable = panel_enable,
    .register_callback = panel_register_callback,
};

/* Init the videomode parameter, dts will override the initial value. */
static struct display_timing icnl9707_timing = {
    .pixelclock = 70000000,
    .hactive = 480,
    .hfront_porch = 50,
    .hback_porch = 50,
    .hsync_len = 20,    // 790
    .vactive = 1920,
    .vfront_porch = 20,
    .vback_porch = 17,
    .vsync_len = 20, // 1952
};

struct panel_dsi dsi = {
    .mode = DSI_MOD_VID_BURST,
    .format = DSI_FMT_RGB888,
    .lane_num = 4,
};

struct aic_panel dsi_icnl9707 = {
    .name = "panel-icnl9707",
    .timings = &icnl9707_timing,
    .funcs = &panel_funcs,
    .dsi = &dsi,
    .connector_type = AIC_MIPI_COM,
};
